export function normalizeRenderMode(renderMode = RENDER_MODES.TRUSTED) {
  if (renderMode === RENDER_MODES.TRUSTED || renderMode === RENDER_MODES.SANITIZED) {
    return renderMode;
  }

  throw new Error(
    `Unknown render mode: ${renderMode}. Use "${RENDER_MODES.TRUSTED}" or "${RENDER_MODES.SANITIZED}".`,
  );
}

function dropUnsafeRawHtmlElements() {
  const blockedTagNames = new Set(['script', 'style', 'iframe', 'object', 'embed']);

  return (tree) => {
    visitChildren(tree, (children, index, node) => {
      if (node.type === 'element' && blockedTagNames.has(node.tagName)) {
        children.splice(index, 1);
        return index;
      }

      return undefined;
    });
  };
}

function rehypeAgentHeadingAnchors(options = {}) {
  return (tree) => {
    const toc = options.toc || [];
    const seenIds = new Map();

    visitChildren(tree, (children, index, node) => {
      if (node?.type !== 'element' || !/^h[1-6]$/.test(node.tagName)) {
        return undefined;
      }

      const text = plainText(node).replace(/\s+/g, ' ').trim();
      if (!text) {
        return undefined;
      }

      node.properties ||= {};
      const level = Number(node.tagName.slice(1));
      const existingId = readStringProperty(node.properties, 'id');
      const id = existingId || uniqueHeadingId(slugifyHeading(text), seenIds);

      if (level <= 3) {
        toc.push({ id, text, level });
      }

      if (!existingId) {
        children.splice(index, 0, {
          type: 'element',
          tagName: 'span',
          properties: { id, className: ['agent-isles-heading-anchor'], ariaHidden: 'true' },
          children: [],
        });
        return index + 2;
      }

      return undefined;
    });
  };
}

function plainText(node) {
  if (!node) {
    return '';
  }
  if (node.type === 'text') {
    return node.value || '';
  }
  if (!Array.isArray(node.children)) {
    return '';
  }
  return node.children.map(plainText).join('');
}

function slugifyHeading(text) {
  const slug = String(text)
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-');

  return slug || 'section';
}

function uniqueHeadingId(baseId, seenIds) {
  const count = seenIds.get(baseId) || 0;
  seenIds.set(baseId, count + 1);
  return count === 0 ? baseId : `${baseId}-${count + 1}`;
}

function visitChildren(node, visitor) {
  if (!Array.isArray(node.children)) {
    return;
  }

  for (let index = 0; index < node.children.length; index += 1) {
    const child = node.children[index];
    const nextIndex = visitor(node.children, index, child, node);

    if (typeof nextIndex === 'number') {
      index = nextIndex - 1;
      continue;
    }

    visitChildren(child, visitor);
  }
}

function rehypeAgentWritebackMetadata(options = {}) {
  return (tree) => {
    const writebackOptions = options.writeback || {};
    const enabled = writebackOptions.enabled === true;
    const markdownTaskMarkers = options.markdownTaskMarkers || [];
    let generatedId = 0;
    let markdownTaskIndex = 0;

    visitChildren(tree, (_children, _index, node, parent) => {
      if (node?.type !== 'element') {
        return undefined;
      }

      const operationType = readWritebackOperationType(node.properties);
      stripWritebackProperties(node.properties);

      if (isMarkdownTaskCheckboxInput(node, parent, _index)) {
        const marker = markdownTaskMarkers[markdownTaskIndex];
        markdownTaskIndex += 1;

        if (!enabled || !marker) {
          return undefined;
        }

        const sourcePath = sourcePathForWriteback(options.sourcePath, writebackOptions.rootPath);
        if (!sourcePath) {
          return undefined;
        }

        delete node.properties.disabled;
        node.properties['aria-label'] = marker.checked ? 'Mark task incomplete' : 'Mark task complete';
        node.properties['data-agent-isles-writeback'] = JSON.stringify({
          contractVersion: WRITEBACK_CONTRACT_VERSION,
          sourcePath,
          sourceVersion: createSourceVersion(options.sourceMarkdown || ''),
          target: {
            kind: 'markdown-task-checkbox',
            tagName: 'input',
            range: marker.range,
            anchor: { text: marker.marker },
          },
          operation: { type: 'markdown:set-task-checkbox' },
        });
        return undefined;
      }

      if (!enabled || !operationType) {
        return undefined;
      }

      if (!isAgentComponentTag(node.tagName) || !node.position?.start || !node.position?.end) {
        return undefined;
      }

      generatedId += 1;
      const componentId = readStringProperty(node.properties, 'id') || `${node.tagName}-${generatedId}`;
      const sourcePath = sourcePathForWriteback(options.sourcePath, writebackOptions.rootPath);
      if (!sourcePath) {
        return undefined;
      }

      const metadata = {
        contractVersion: WRITEBACK_CONTRACT_VERSION,
        sourcePath,
        sourceVersion: createSourceVersion(options.sourceMarkdown || ''),
        target: {
          componentId,
          tagName: node.tagName,
          range: {
            start: copyPositionPoint(node.position.start),
            end: copyPositionPoint(node.position.end),
          },
        },
        operation: { type: operationType },
      };

      node.properties['data-agent-isles-writeback'] = JSON.stringify(metadata);
      return undefined;
    });
  };
}

function isMarkdownTaskCheckboxInput(node, parent, index) {
  if (node?.tagName !== 'input') {
    return false;
  }

  const properties = node.properties || {};
  const isCheckbox = readStringProperty(properties, 'type') === 'checkbox' || properties.type === 'checkbox';
  if (!isCheckbox || !Object.hasOwn(properties, 'disabled')) {
    return false;
  }

  return index === 0 && parent?.tagName === 'li' && hasClassName(parent.properties, 'task-list-item');
}

function hasClassName(properties = {}, className) {
  const value = properties.className;
  if (Array.isArray(value)) {
    return value.includes(className);
  }
  if (typeof value === 'string') {
    return value.split(/\s+/).includes(className);
  }
  return false;
}

function buildMarkdownTaskMarkerRecords(markdown) {
  const records = [];
  const source = String(markdown || '');
  const linePattern = /.*(?:\r\n|\n|\r|$)/g;
  let match;

  while ((match = linePattern.exec(source)) !== null) {
    const lineWithEnding = match[0];
    if (lineWithEnding === '') {
      break;
    }

    const lineOffset = match.index;
    const lineNumber = recordsLineNumber(source, lineOffset);
    const lineWithoutEnding = lineWithEnding.replace(/\r\n|\n|\r$/, '');
    const taskMatch = lineWithoutEnding.match(/^([ \t]*(?:[-+*]|\d+[.)])[ \t]+)(\[[ xX]\])(?=\s|$)/);
    if (taskMatch) {
      const markerOffset = lineOffset + taskMatch[1].length;
      const markerColumn = taskMatch[1].length + 1;
      const marker = taskMatch[2];
      records.push({
        marker,
        checked: marker === '[x]' || marker === '[X]',
        range: {
          start: { line: lineNumber, column: markerColumn, offset: markerOffset },
          end: { line: lineNumber, column: markerColumn + marker.length, offset: markerOffset + marker.length },
        },
      });
    }

    if (linePattern.lastIndex >= source.length) {
      break;
    }
  }

  return records;
}

function recordsLineNumber(source, offset) {
  let line = 1;
  for (let index = 0; index < offset; index += 1) {
    if (source[index] === '\n') {
      line += 1;
    }
  }
  return line;
}

