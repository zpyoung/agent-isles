function buildSanitizedSchema(resolvedPacks) {
  const tagNames = new Set(coreSanitizedSchema.tagNames || []);
  const attributes = copySchemaAttributes(coreSanitizedSchema.attributes);

  for (const pack of resolvedPacks?.packs || []) {
    for (const tag of pack.tags || []) {
      if (!tag?.name) {
        continue;
      }

      tagNames.add(tag.name);
      const declaredAttributes = (tag.attributes || []).filter(isSafePackDeclaredAttribute);
      if (declaredAttributes.length === 0) {
        continue;
      }

      attributes[tag.name] = [
        ...new Set([
          ...(attributes[tag.name] || []),
          ...declaredAttributes,
        ]),
      ];
    }
  }

  return {
    ...coreSanitizedSchema,
    tagNames: [...tagNames],
    attributes,
  };
}

function copySchemaAttributes(attributes = {}) {
  return Object.fromEntries(
    Object.entries(attributes).map(([tagName, allowedAttributes]) => [
      tagName,
      [...allowedAttributes],
    ]),
  );
}

function isSafePackDeclaredAttribute(attributeName) {
  const normalizedName = String(attributeName).toLowerCase();

  return !normalizedName.startsWith('on') && normalizedName !== 'style' && normalizedName !== 'srcdoc';
}

export async function renderMarkdown(markdown, options = {}) {
  const renderMode = normalizeRenderMode(options.renderMode);
  const assetMode = normalizeAssetMode(options.assetMode);
  const sourceMarkdown = typeof options.sourceMarkdown === 'string' ? options.sourceMarkdown : markdown;
  const markdownTaskMarkers = buildMarkdownTaskMarkerRecords(sourceMarkdown);
  const toc = [];
  const processor = unified()
    .use(remarkParse)
    .use(remarkGfm)
    .use(remarkRehype, { allowDangerousHtml: true })
    .use(rehypeAgentMermaid)
    .use(rehypeAgentD2)
    .use(rehypeRaw)
    .use(rehypeAgentWritebackMetadata, {
      ...options,
      sourceMarkdown,
      markdownTaskMarkers,
    });

  if (renderMode === RENDER_MODES.SANITIZED) {
    processor
      .use(dropUnsafeRawHtmlElements)
      .use(rehypeSanitize, buildSanitizedSchema(options.resolvedPacks));
  }

  processor.use(rehypeAgentHeadingAnchors, { toc });

  const body = await processor
    .use(rehypeHighlight)
    .use(rehypeStringify, { allowDangerousHtml: renderMode === RENDER_MODES.TRUSTED })
    .process(markdown);

  return buildHtmlPage(String(body), { ...options, renderMode, assetMode, sourceMarkdown, toc });
}

export async function renderMarkdownString(markdown, options = {}) {
  const assetMode = normalizeAssetMode(options.assetMode);
  const resolvedPacks = await resolvePackInputs({
    explicitPacks: options.explicitPacks || [],
    projectDir: options.projectDir ?? process.cwd(),
    includeUserPacks: options.includeUserPacks === true,
    userConfigDir: options.userConfigDir || null,
  });
  const packAssetRecords = buildPackAssetRecords(resolvedPacks.packs);
  const html = await renderMarkdown(markdown, {
    ...options,
    assetMode,
    resolvedPacks,
    packAssetRecords,
  });

  return { html, resolvedPacks, packAssetRecords, assetMode };
}

export async function renderMarkdownFile(inputPath, options = {}) {
  const filePath = validateMarkdownInput(inputPath);
  const markdown = readFileSync(filePath, 'utf8');
  const outFile = options.outFile ? resolve(options.outFile) : undefined;
  const { html, resolvedPacks, packAssetRecords, assetMode } = await renderMarkdownString(markdown, {
    ...options,
    sourcePath: filePath,
    projectDir: options.projectDir ?? dirname(filePath),
  });

  if (outFile) {
    mkdirSync(dirname(outFile), { recursive: true });
    writeFileSync(outFile, html);
    if (assetMode !== 'inline') {
      copyComponentBundle(dirname(outFile));
      if (assetMode === 'local') {
        copyLocalAssets(dirname(outFile));
        if (hasMermaidDiagrams(html)) {
          copyMermaidRuntime(dirname(outFile));
        }
      }
      copyPackAssets(dirname(outFile), packAssetRecords);
      writePackMetadata(dirname(outFile), packAssetRecords);
    }
  }

  return { html, outFile, resolvedPacks };
}

function rehypeAgentMermaid() {
  return (tree) => {
    transformMermaidCodeBlocks(tree);
  };
}

function transformMermaidCodeBlocks(node) {
  if (!Array.isArray(node.children)) {
    return;
  }

  for (let index = 0; index < node.children.length; index += 1) {
    const child = node.children[index];
    const mermaidCode = extractLanguageCodeBlock(child, 'mermaid');

    if (mermaidCode) {
      node.children[index] = {
        type: 'element',
        tagName: 'figure',
        properties: { className: ['agent-mermaid'], dataAgentMermaid: true },
        children: [
          {
            type: 'element',
            tagName: 'pre',
            properties: { className: ['mermaid'], dataAgentMermaidSource: true },
            children: [{ type: 'text', value: mermaidCode.value }],
          },
        ],
      };
      continue;
    }

    transformMermaidCodeBlocks(child);
  }
}

function rehypeAgentD2() {
  return async (tree) => {
    await transformD2CodeBlocks(tree);
  };
}

async function transformD2CodeBlocks(node) {
  if (!Array.isArray(node.children)) {
    return;
  }

  for (let index = 0; index < node.children.length; index += 1) {
    const child = node.children[index];
    const d2Code = extractLanguageCodeBlock(child, 'd2');

    if (d2Code) {
      const svg = await renderD2Svg(d2Code.value, child.position);
      node.children[index] = {
        type: 'element',
        tagName: 'figure',
        properties: { className: ['beoe', 'd2'] },
        children: [{ type: 'raw', value: svg }],
      };
      continue;
    }

    await transformD2CodeBlocks(child);
  }
}

function extractLanguageCodeBlock(node, language) {
  if (node?.type !== 'element' || node.tagName !== 'pre') {
    return null;
  }

  const codeNode = node.children?.find((child) => child.type === 'element' && child.tagName === 'code');
  const classNames = codeNode?.properties?.className || [];
  const languageClassName = `language-${language}`;
  const hasLanguage = Array.isArray(classNames)
    ? classNames.includes(languageClassName)
    : String(classNames).split(/\s+/).includes(languageClassName);

  if (!hasLanguage) {
    return null;
  }

  return {
    value: codeNode.children?.map((child) => child.value || '').join('') || '',
  };
}

async function renderD2Svg(source, position) {
  const d2 = new D2();

  try {
    const result = await d2.compile(source, { noXMLTag: true });
    return await d2.render(result.diagram, { ...result.renderOptions, noXMLTag: true });
  } catch (error) {
    const location = formatPosition(position);
    const message = error?.message || String(error);
    throw new Error(`D2 diagram render failed${location}: ${message}`);
  } finally {
    await d2.worker?.terminate?.();
  }
}

function formatPosition(position) {
  const start = position?.start;
  if (!start?.line) {
    return '';
  }

  return ` at line ${start.line}${start.column ? `, column ${start.column}` : ''}`;
}

